export function getGalleryPage(page: number = 1, itemsPerPage: number = 12) {
  const startIndex = (page - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const items = galleryData.slice(startIndex, endIndex);
  const totalPages = Math.ceil(galleryData.length / itemsPerPage);

  return {
    items,
    total: galleryData.length,
    totalPages,
    currentPage: page
  };
}

export function getGalleryByCategory(category: string) {
  return galleryData.filter(item => item.category === category);
}

export function getAllCategories() {
  return Array.from(new Set(galleryData.map(item => item.category)));
}
