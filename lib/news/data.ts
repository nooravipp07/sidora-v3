export function getNewsBySlug(slug: string): News | undefined {
  return newsData.find(news => news.slug === slug);
}

export function getNewsPage(page: number, itemsPerPage: number = 12) {
  const startIndex = (page - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  
  return {
    items: newsData.slice(startIndex, endIndex),
    total: newsData.length,
    totalPages: Math.ceil(newsData.length / itemsPerPage),
    currentPage: page,
  };
}

export function getFeaturedNews(): News {
  // Return the first/most recent news as featured
  return newsData[0];
}
